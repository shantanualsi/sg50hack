package sg.com.sghack.data.v2;

public class Bus {
	private int nbus;
	private String name;
	private int cwd;
	public int getNbus() {
		return nbus;
	}
	public void setNbus(int nbus) {
		this.nbus = nbus;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCwd() {
		return cwd;
	}
	public void setCwd(int cwd) {
		this.cwd = cwd;
	}
}
